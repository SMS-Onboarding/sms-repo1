// Variables
let greeting = "Hello";
const name = "World";
let count = 0;

// Function to print a message
function printMessage(message) {
    console.log(message);
}

// Conditional to check the greeting
if (greeting === "Hello") {
    printMessage(`${greeting}, ${name}!`);
} else {
    printMessage("Goodbye!");
}

// Loop to count to 5
for (let i = 1; i <= 5; i++) {
    count++;
    console.log(`Count is: ${count}`);
}

